#include <unistd.h>

void	ft_print_comb(void)
{
	char	a1;
	char	a2;
	char	a3;

	a1 = '0';
	while (a1 <= '7')
	{
		a2 = a1 + 1;
		while (a2 <= '8')
		{
			a3 = a2 + 1;
			while (a3 <= '9')
			{
				write (1, &a1, 1);
				write (1, &a2, 1);
				write (1, &a3, 1);
				if (a1 != '7')
					write (1, ", ", 2);
				a3++;
			}
			a2++;
		}
		a1++;
	}
}
